"""
Конвертация настроек тона (0-100) в текстовый блок для промпта Claude.
Каждый порог — заранее написанная формулировка, чтобы Claude не интерпретировал «20%» на глаз.
"""

LENGTH_HINT = {
    "short":  "200-400 символов",
    "medium": "500-900 символов",
    "long":   "1000-1500 символов",
}


def _formality(v: int) -> str:
    if v < 20: return "почти разговорный, без канцелярита и сложных конструкций"
    if v < 40: return "нейтрально, без жаргона, но и без официоза"
    if v < 60: return "лёгкая официальность, профессиональный тон"
    if v < 80: return "деловой стиль, выверенные формулировки"
    return "максимально формально, как в официальной документации"


def _friendliness(v: int) -> str:
    if v < 20: return "отстранённо, без личного контакта с читателем"
    if v < 40: return "нейтрально, без обращений на «ты»"
    if v < 60: return "доброжелательно, читатель — собеседник"
    if v < 80: return "по-приятельски, обращение на «ты», лёгкие шутки"
    return "как разговор с близким другом — на «ты», со внутренними шутками и тёплыми обращениями"


def _emoji(v: int) -> str:
    if v < 15: return "без эмодзи вообще"
    if v < 35: return "редко, только в смысловых местах (1 эмодзи на пост максимум)"
    if v < 60: return "умеренно, 2-3 эмодзи на пост"
    if v < 80: return "часто, в каждом смысловом блоке"
    return "очень часто, эмодзи в каждом смысловом маркере"


def _personal(v: int) -> str:
    if v < 20: return "только факты и наблюдения, без личного опыта"
    if v < 40: return "минимум личного, в основном объективная подача"
    if v < 60: return "иногда вставлять личные наблюдения для иллюстрации"
    if v < 80: return "опирайся на личный опыт автора и конкретные истории из его жизни"
    return "пост построен на личной истории автора, от первого лица — это главный носитель идеи"


def _concrete(v: int) -> str:
    if v < 20: return "обобщённо и концептуально, без деталей"
    if v < 40: return "общие принципы, мало конкретики"
    if v < 60: return "сбалансированно — концепции с примерами"
    if v < 80: return "конкретика: цифры, имена, кейсы, точные детали"
    return "максимум конкретики: имена, даты, цифры, прямые цитаты"


def _engagement(v: int) -> str:
    if v < 20: return "монолог, без обращения к читателю"
    if v < 40: return "минимум вопросов и обращений к читателю"
    if v < 60: return "пара точек контакта с читателем по тексту"
    if v < 80: return "вопрос или CTA в конце поста, по тексту обращения к читателю"
    return "активное вовлечение: вопросы, призывы поделиться опытом, обращения по тексту"


def _sources(v: int) -> str:
    # ⚠️ Пока без web_search — софтовые формулировки без выдуманных URL
    if v < 15: return "не ссылаться на внешние источники"
    if v < 35: return "если уместно — мимоходом упомяни 1 факт/событие из реальной жизни (без выдуманных URL)"
    if v < 60: return "1-2 ключевых утверждения подкрепи упоминанием реального события/исследования (без выдуманных URL)"
    if v < 80: return "большинство ключевых утверждений подкрепляй конкретными фактами/упоминаниями (без выдуманных URL)"
    return "каждое ключевое утверждение требует подкрепления реальным фактом или упоминанием (без выдуманных URL)"


_FORMATTERS = {
    "formality":   _formality,
    "friendliness": _friendliness,
    "emoji":       _emoji,
    "personal":    _personal,
    "concrete":    _concrete,
    "engagement":  _engagement,
    "sources":     _sources,
}

_TITLES = {
    "formality":    "Формальность",
    "friendliness": "Дружелюбие",
    "emoji":        "Эмодзи",
    "personal":     "Личный опыт",
    "concrete":     "Конкретика",
    "engagement":   "Вовлечение читателя",
    "sources":      "Подкрепление источниками",
}


def format_for_prompt(settings: dict) -> str:
    """Превращает настройки в текстовый блок для промпта."""
    if not settings:
        return ""
    lines = ["НАСТРОЙКИ ТОНА ПОСТА:"]
    for key, fn in _FORMATTERS.items():
        v = settings.get(key)
        if v is None:
            continue
        lines.append(f"- {_TITLES[key]} ({v}/100): {fn(int(v))}")
    length = settings.get("length")
    if length in LENGTH_HINT:
        lines.append(f"- Длина поста: {LENGTH_HINT[length]}")
    return "\n".join(lines)
